#include "3-calc.h"

/**
 * op_add - add a & b
 * @a: int val
 * @b: int val
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub a & b
 * @a: int val
 * @b: int val
 * Return: difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mult a & b
 * @a: int val
 * @b: int val
 * Return: product
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide a & b
 * @a: int val
 * @b: int val
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo a & b
 * @a: int val
 * @b: int val
 * Return: modulo
 */
int op_mod(int a, int b)
{
	return (a % b);
}
